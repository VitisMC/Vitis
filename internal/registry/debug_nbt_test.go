func TestDumpEnchantmentNBT(t *testing.T) {
	data := genreg.ConfigRegistryData()
	enchEntries, ok := data["minecraft:enchantment"]
	if !ok {
		t.Fatal("no minecraft:enchantment in config data")
	}

	for _, entry := range enchEntries {
		if entry.Name != "minecraft:binding_curse" {
			continue
		}
		t.Logf("Entry: %s, %d bytes", entry.Name, len(entry.Data))
		t.Logf("Hex:\n%s", hex.Dump(entry.Data))

		// Verify first byte is TAG_Compound (0x0a)
		if len(entry.Data) < 1 || entry.Data[0] != 0x0a {
			t.Errorf("first byte is 0x%02x, expected 0x0a (TAG_Compound)", entry.Data[0])
		}
		return
	}
	t.Fatal("minecraft:binding_curse not found")
}

